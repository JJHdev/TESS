CREATE OR REPLACE FUNCTION TESSUSER.FN_FMT_STRTODATE (
    /**
     * =============================================================
     * name   : 날짜 형식 변경
     * author : ntarget
     * date   : 2014.03.31
     * tables : NONE
     * -------------------------------------------------------------
     *
     * @param     vi_strdate        입력값
     * @param     vi_gbn            구분자
     * @return    vo_date           포맷 데이터
     * =============================================================
     */
    vi_strdate              VARCHAR2,
    vi_gbn                  VARCHAR2
)
RETURN VARCHAR2
IS
    vo_date          VARCHAR2(100);
    v_strExpDate     VARCHAR2(100);

BEGIN
    v_strExpDate := REGEXP_REPLACE(vi_strdate, '[^0-9]+', '');

    IF LENGTH(TRIM(v_strExpDate)) = 8 THEN
        vo_date     := REGEXP_REPLACE(v_strExpDate, '(.{4})(.{2})(.{2})', '\1'||vi_gbn||'\2'||vi_gbn||'\3');
    ELSIF LENGTH(TRIM(v_strExpDate)) = 6 THEN
        vo_date     := REGEXP_REPLACE(v_strExpDate, '(.{4})(.{2})', '\1'||vi_gbn||'\2');
    ELSE
        vo_date := v_strExpDate;
    END IF;

    RETURN vo_date;
END FN_FMT_STRTODATE;

