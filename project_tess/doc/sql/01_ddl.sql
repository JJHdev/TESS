CREATE OR REPLACE FUNCTION TESSUSER.FN_FMT_DATA (
    /**
     * =============================================================
     * name   : 포맷된 데이터 조회(전화번호, 사업자등록번호).
     * author : ntarget
     * date   : 2014.03.31
     * tables : NONE
     * -------------------------------------------------------------
     *
     * @param     vi_data           입력값
     * @param     vi_type           포맷타입(전화번호 TEL, 사업자등록번호 RNO)
     * @return    vo_fmt_data        포맷 데이터
     * =============================================================
     */
    vi_type              VARCHAR2,
    vi_data              VARCHAR2
)
RETURN VARCHAR2
IS
    vo_fmt_data          VARCHAR2(100);

BEGIN
    vo_fmt_data := null;

    IF TRIM(vi_data) IS NOT NULL THEN
        IF TRIM(vi_type) = 'TEL' THEN
            vo_fmt_data := REGEXP_REPLACE(REGEXP_REPLACE(vi_data,'[^:digit:]]'),'(^02|050[[:digit:]]{1}|[[:digit:]]{3})([[:digit:]]{3,4})([[:digit:]]{4})','\1-\2-\3');
        ELSIF TRIM(vi_type) = 'RNO' THEN
            vo_fmt_data := REGEXP_REPLACE(vi_data, '^([0-9]{3})-?([0-9]{2})-?([0-9]{5})$', '\1-\2-\3');
        ELSIF TRIM(vi_type) = 'NUM' THEN
            vo_fmt_data := REGEXP_REPLACE(vi_data, '[^0-9]+', '');
        ELSIF TRIM(vi_type) = 'NUM2' THEN
            vo_fmt_data := REGEXP_REPLACE(vi_data, '[^.0-9]+', '');
        END IF;
    END IF;

    RETURN vo_fmt_data;
END FN_FMT_DATA;

